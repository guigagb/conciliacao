export const nameMonthToMonth = (name) => {
    switch (name) {
        case 'JAN': return 1
        case 'FEV': return 2
        case 'MAR': return 3
        case 'ABR': return 4
        case 'MAI': return 5
        case 'JUN': return 6
        case 'JUL': return 7
        case 'AGO': return 8
        case 'SET': return 9
        case 'OUT': return 10
        case 'NOV': return 11
        case 'DEZ': return 12
    }
}